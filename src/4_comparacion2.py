calif_parcial = int( input("Parcial: ") )
CALIF_APROB = 70

print( calif_parcial, type(calif_parcial) )
print( CALIF_APROB,   type(CALIF_APROB)   )

print(calif_parcial > CALIF_APROB)

print( str(calif_parcial) + " > "  + str(CALIF_APROB) + " = ", calif_parcial > CALIF_APROB)
print( str(calif_parcial) + " >= " + str(CALIF_APROB) + " = ", calif_parcial >= CALIF_APROB)
print( str(calif_parcial) + " < "  + str(CALIF_APROB) + " = ", calif_parcial <  CALIF_APROB)
print( str(calif_parcial) + " <= " + str(CALIF_APROB) + " = ", calif_parcial <= CALIF_APROB)
print( str(calif_parcial) + " == " + str(CALIF_APROB) + " = ", calif_parcial == CALIF_APROB)
print( str(calif_parcial) + " != " + str(CALIF_APROB) + " = ", calif_parcial != CALIF_APROB)





